<?php
	$a = 10;

	//++ operador de incremento, soma 1 à variável, depois da execução
	echo $a++;
	echo "<br/>";
	echo $a;

	echo "<br/><br/>";

	//++ operador de incremento, soma 1 à variável, antes da execução
	echo ++$a;
	echo "<br/>";
	echo $a;

	echo "<br/><br/>";

	//-- operador de decremento, subtrai 1 à variável, depois da execução
	echo $a--;
	echo "<br/>";
	echo $a;

	echo "<br/><br/>";

	//-- operador de decremento, subtrai 1 à variável, antes da execução
	echo --$a;
	echo "<br/>";
	echo $a;
?>