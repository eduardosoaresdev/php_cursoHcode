<?php
	//Operadores aritiméticos
	$a = 10;
	$b = 2;

	echo "Para A = " . $a . " e B = " . $b . ", temos: <br/><br/>";
	echo "Adição >>> ";
	echo $a + $b . "<br/><br/>";

	echo "Subtração >>> ";
	echo $a - $b . "<br/><br/>";

	echo "Multiplicação >>> ";
	echo $a * $b . "<br/><br/>";

	echo "Divisão >>> ";
	echo $a / $b . "<br/><br/>";	

	//% - Módulo (resto da divisão)
	echo "Módulo >>> ";
	echo $a % $b . "<br/><br/>";

	//** - Potenciação
	echo "Potenciação >>> ";
	echo $a ** $b . "<br/><br/>";
?>