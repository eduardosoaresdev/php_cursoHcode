<?php
	$nome = "eduardo soares dev";

	//STRTOUPPER deixa todas as letras em maiúsculo
	echo strtoupper($nome);

	echo "<br/><br/>";

	//STRTOLOWER deixa todas as letras em minúsculo
	echo strtolower($nome);

	echo "<br/><br/>";

	//UCFIRST deixa apenas a primeira letra da primeira palavra em maiúsculo
	echo ucfirst($nome);

	echo "<br/><br/>";

	//UCWORDS deixa a primeira letra de cada palavra em maiúsculo
	echo ucwords($nome);
?>